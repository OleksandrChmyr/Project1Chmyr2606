package Hometask3;

public class MathExample {

    public static int calcSum(int a, int b) {
        return a + b;
    }

    public static int calcDiff(int c, int d) {
        return c - d;
    }

    public double calcMul(double e, double f) {
        return e * f;
    }

    public double calcDiv(double g, double h) {
        return g / h;
    }
}
